static uint8_t* glyph(uint8_t ascii) {
    uint8_t *data = (uint8_t *)glyphs;
    data += (int)ascii - 32 * 8;
    return data;
}

static int drawCharactor(int x, int y, uint8_t ascii, uint32_t color, uint32_t paperColor) {
    if (ascii < 32 || ascii > 127) return 0;
    uint8_t *data = (uint8_t *)glyph(ascii);
    
    for (int l=0; l<8; l++) {
        for (int i=7; i>=0; i--) {
            screen::pixel(x + (7 - i), y + l, data[l] & (1 << i) ? color : paperColor);
        }
    }
    return 8;
}

void charset::drawText(const char* text, int x, int y, uint32_t color, uint32_t paperColor) {
    uint8_t *s = (uint8_t*)text;
    while (*s != 0) {
        x += drawCharactor(x, y, *s++, color, paperColor);
    }
}
